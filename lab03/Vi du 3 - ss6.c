#include<stdio.h>
#include<conio.h>
int main(void)
{
		printf("The number 555.55 in various form: \n");
	print("In float from with out modifiers: \n");
		printf("[%f]\n,555.55");
		printf("In exponential from with out any modifier: \n");
		printf("[%e]\n",555.55);
		printf("In float from with -modifier: \n");
		printf("[%f\n]",555.55);
		printf("In float from with digit string 10.3 as modifier\n");
		printf("[%10.3f]\n",555.55);
		printf("In float from with 0 as modifier: \n");
		printf("[%0f]\n",555.55);
		
}
